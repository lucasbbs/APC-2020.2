def area(arg1, arg2, string):
    if string == "triangulo":
        print(f'O {string} tem {arg1*arg2/2:.0f} de area')
    if string == "losango":
        print(f'O {string} tem {arg1*arg2/2:.0f} de area')
    if string == "retangulo":
        print(f'O {string} tem {arg1*arg2:.0f} de area')
    if string == "circulo":
        print(f'O {string} tem {arg1**2*arg2:.0f} de area')
